/* global console, process */

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { normalizeCatalogItem } from './catalog-item-transformer.js';
import {
  getGameItemsSourceLabel,
  getSpritesSourceLabel,
  loadGameItemsMap,
  loadSpriteIds,
} from './game-items-source.js';

const currentFilePath = fileURLToPath(import.meta.url),
  backendDirectoryPath = path.dirname(currentFilePath),
  postgresDirectoryPath = path.resolve(backendDirectoryPath, '..', 'postgres'),
  seedFilePath = path.resolve(postgresDirectoryPath, '002_seed_game_items.sql');

function escapeSqlString(value) {
  return String(value).replace(/'/g, "''");
}

function toSqlString(value) {
  return `'${escapeSqlString(value)}'`;
}

function toSqlNullableString(value) {
  if (value === undefined || value === null || value === '') return 'NULL';
  return toSqlString(value);
}

function toSqlBoolean(value) {
  return value ? 'TRUE' : 'FALSE';
}

function toSqlInteger(value) {
  if (value === undefined || value === null || Number.isNaN(Number(value))) return 'NULL';
  return String(Math.trunc(Number(value)));
}

function toSqlJson(value) {
  return `${toSqlString(JSON.stringify(value))}::jsonb`;
}

function buildInsertTuple(item) {
  return `(
    ${toSqlString(item.itemId)},
    ${toSqlString(item.name)},
    ${toSqlNullableString(item.imageKey)},
    ${toSqlNullableString(item.imagePath)},
    ${toSqlNullableString(item.imageSource)},
    ${toSqlString(item.category)},
    ${toSqlNullableString(item.subcategory)},
    ${toSqlNullableString(item.requiredSkill)},
    ${toSqlInteger(item.requiredLevel)},
    ${toSqlNullableString(item.weaponType)},
    ${toSqlJson(item.attackStats)},
    ${toSqlJson(item.defenseStats)},
    ${toSqlJson(item.bonuses)},
    ${toSqlJson(item.statusSummary)},
    ${toSqlNullableString(item.description)},
    ${toSqlBoolean(item.stackable)},
    ${toSqlInteger(item.maxStack)},
    ${toSqlNullableString(item.projectileName)},
    ${toSqlJson(item.metadataRaw)},
    ${toSqlJson(item.metadataGame)},
    ${toSqlBoolean(
      item.isImageMapped,
    )}, -- Itens sem imagem são automaticamente marcados como não vendáveis
    ${
      item.isImageMapped ? toSqlNullableString(item.priceBrl) : 'NULL'
    }, -- Apenas itens com imagem podem ter preço
    ${toSqlBoolean(item.isImageMapped)},
    NOW(),
    NOW(),
    NOW()
  )`;
}

function buildSeedSql(items) {
  let generatedAt = new Date().toISOString(),
    tuples = items.map((item) => buildInsertTuple(item)).join(',\n');

  return `-- Auto-generated by Marketplace/backend/generate-seed-sql.js
-- Source items: ${getGameItemsSourceLabel()}
-- Source sprites: ${getSpritesSourceLabel()}
-- Generated at: ${generatedAt}
-- Re-run the generator instead of editing this file manually.

INSERT INTO catalog_items (
  item_id,
  name,
  image_key,
  image_path,
  image_source,
  category,
  subcategory,
  required_skill,
  required_level,
  weapon_type,
  attack_stats,
  defense_stats,
  bonuses,
  status_summary,
  description,
  stackable,
  max_stack,
  projectile_name,
  metadata_raw,
  metadata_game,
  can_sell,
  price_brl,
  is_image_mapped,
  last_imported_at,
  created_at,
  updated_at
)
VALUES
${tuples}
ON CONFLICT (item_id) DO UPDATE SET
  name = EXCLUDED.name,
  image_key = EXCLUDED.image_key,
  image_path = EXCLUDED.image_path,
  image_source = EXCLUDED.image_source,
  category = EXCLUDED.category,
  subcategory = EXCLUDED.subcategory,
  required_skill = EXCLUDED.required_skill,
  required_level = EXCLUDED.required_level,
  weapon_type = EXCLUDED.weapon_type,
  attack_stats = EXCLUDED.attack_stats,
  defense_stats = EXCLUDED.defense_stats,
  bonuses = EXCLUDED.bonuses,
  status_summary = EXCLUDED.status_summary,
  description = EXCLUDED.description,
  stackable = EXCLUDED.stackable,
  max_stack = EXCLUDED.max_stack,
  projectile_name = EXCLUDED.projectile_name,
  metadata_raw = EXCLUDED.metadata_raw,
  metadata_game = EXCLUDED.metadata_game,
  is_image_mapped = EXCLUDED.is_image_mapped,
  can_sell = EXCLUDED.is_image_mapped, -- Itens sem imagem são automaticamente marcados como não vendáveis
  price_brl = CASE WHEN EXCLUDED.is_image_mapped THEN EXCLUDED.price_brl ELSE NULL END, -- Apenas itens com imagem podem ter preço
  last_imported_at = NOW(),
  updated_at = NOW();
`;
}

export async function generateSeedSqlFile() {
  let [itemsMap, spriteIds] = await Promise.all([loadGameItemsMap(), loadSpriteIds()]),
    normalizedItems = Object.entries(itemsMap)
      .sort(([leftKey], [rightKey]) => leftKey.localeCompare(rightKey))
      .map(([gameItemKey, itemData]) => normalizeCatalogItem(gameItemKey, itemData, spriteIds)),
    sql = buildSeedSql(normalizedItems);

  await fs.mkdir(postgresDirectoryPath, { recursive: true });
  await fs.writeFile(seedFilePath, sql, 'utf8');

  return {
    outputPath: seedFilePath,
    itemCount: normalizedItems.length,
    mappedImages: normalizedItems.filter((item) => item.isImageMapped).length,
    missingImages: normalizedItems.filter((item) => !item.isImageMapped).length,
  };
}

async function run() {
  let summary = await generateSeedSqlFile();

  console.log(
    JSON.stringify(
      {
        generatedSeed: 'Marketplace/postgres/002_seed_game_items.sql',
        sourceItems: getGameItemsSourceLabel(),
        sourceSprites: getSpritesSourceLabel(),
        itemCount: summary.itemCount,
        mappedImages: summary.mappedImages,
        missingImages: summary.missingImages,
      },
      null,
      2,
    ),
  );
}

if (process.argv[1] && currentFilePath === path.resolve(process.argv[1]))
  run().catch((error) => {
    console.error('Falha ao gerar seed SQL do catálogo real:', error);
    process.exitCode = 1;
  });
